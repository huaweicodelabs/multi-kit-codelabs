package com.huawei.hms.urbanhomeservices.java.model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;
import com.huawei.agconnect.cloud.database.CloudDBZoneObject;

/**
 * This model call is used to fetch ServiceCategory list from Cloud DB.
 *
 * @author: Huawei
 * @since : 20-01-2021
 */

public class ServiceType extends CloudDBZoneObject {

    @SerializedName("cat_name")
    @Expose
    private String catName;
    @SerializedName("id")
    @Expose
    private String id;
    @SerializedName("name")
    @Expose
    private String name;
    @SerializedName("image_name")
    @Expose
    private String imageName;

    public ServiceType(Class<? extends CloudDBZoneObject> aClass) {
        super(aClass);
    }

    public String getCatName() {
        return catName;
    }

    public void setCatName(String catName) {
        this.catName = catName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImageName() {
        return imageName;
    }

    public void setImageName(String imageName) {
        this.imageName = imageName;
    }
}
